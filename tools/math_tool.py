def add(a: float, b: float) -> float:
    """Add two numbers."""
    result = a + b
    print(f"Math tool: Adding {a} + {b} = {result}")
    return result

def multiply(a: float, b: float) -> float:
    """Multiply two numbers."""
    result = a * b
    print(f"Math tool: Multiplying {a} * {b} = {result}")
    return result
